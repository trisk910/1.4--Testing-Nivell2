package Nivell1.Bussiness;

import java.util.ArrayList;

public class Collection {
    private String name;
    private ArrayList<Book> books;

    public Collection(String name) {
        this.name = name;
        this.books = books;
    }

    public String getName() {
        return name;
    }

    public ArrayList<Book> getBooks() {
        return books;
    }
}
